/**
 * Simple server to run the E-Waste Reporting Demo
 */

const http = require('http');
const fs = require('fs');
const path = require('path');

const PORT = 9000;

const mimeTypes = {
  '.html': 'text/html',
  '.css': 'text/css',
  '.js': 'text/javascript',
  '.json': 'application/json',
  '.png': 'image/png',
  '.jpg': 'image/jpg',
  '.gif': 'image/gif',
  '.svg': 'image/svg+xml',
  '.ico': 'image/x-icon'
};

const server = http.createServer((req, res) => {
  let filePath = '.' + req.url;
  if (filePath === './') {
    filePath = './demo.html';
  } else if (filePath === './full-e-waste-report.html') {
    filePath = './full-e-waste-report.html';
  }

  const extname = String(path.extname(filePath)).toLowerCase();
  const contentType = mimeTypes[extname] || 'application/octet-stream';

  fs.readFile(filePath, (error, content) => {
    if (error) {
      if (error.code === 'ENOENT') {
        res.writeHead(404);
        res.end('File not found');
      } else {
        res.writeHead(500);
        res.end('Server error: ' + error.code);
      }
    } else {
      res.writeHead(200, { 'Content-Type': contentType });
      res.end(content, 'utf-8');
    }
  });
});

server.listen(PORT, () => {
  console.log(`
🌱 E-Waste Reporting System Demo Server
=====================================
Server is running at: http://localhost:${PORT}

Open your browser and go to the link above to view the demo.
Press Ctrl+C to stop the server.
  `);
}); 